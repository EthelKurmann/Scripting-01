def rom(zahl):
    if zahl == 1:
        return "I"
    elif zahl == 2:
        return "II"
    elif zahl == 3:
        return "III"
    elif zahl == 4:
        return "IV"
    elif zahl == 5:
        return "V"
    elif zahl == 6:
        return "VI"
    elif zahl == 7:
        return "VII"
    elif zahl == 8:
        return "VIII"
    elif zahl == 9:
        return "IX"
    elif zahl == 10:
        return "X"

